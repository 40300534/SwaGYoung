package com.breeze.pojo;

/**
 * 用户类型
 * @author wdnnccey
 */
public class UserType {

    //用户类别Id
    private Integer id;

    //用户类别名称
    private String name;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
